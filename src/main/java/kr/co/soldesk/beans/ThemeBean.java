package kr.co.soldesk.beans;

public class ThemeBean {

	private String theme_name;
	private int theme_index;
	private int theme_code;
	public String getTheme_name() {
		return theme_name;
	}
	public void setTheme_name(String theme_name) {
		this.theme_name = theme_name;
	}
	public int getTheme_index() {
		return theme_index;
	}
	public void setTheme_index(int theme_index) {
		this.theme_index = theme_index;
	}
	public int getTheme_code() {
		return theme_code;
	}
	public void setTheme_code(int theme_code) {
		this.theme_code = theme_code;
	}
	
	

	
}
